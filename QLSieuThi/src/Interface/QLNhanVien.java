    private void BangNhanVien() {
        try {
            String userName = "sa";
            String password = "123456";
            String url = "jdbc:sqlserver://localhost:1433;databaseName=QLSieuThi;";
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection conn = java.sql.DriverManager.getConnection(url, userName, password);
            String sql = "select * from NHANVIEN";
            PreparedStatement ps = conn.prepareStatement(sql);

            ResultSet rs = ps.executeQuery();
            tableModel.setRowCount(0);
            while (rs.next()) {
                String[] row = new String[]{
                    rs.getString("MaNV"), 
                    rs.getString("TenNV"), 
                    rs.getString("GioiTinh"), 
                    rs.getString("NamSinh"), 
                    rs.getString("SDTNV"), 
                    rs.getString("DiaChiNV")
                };
                tableModel.addRow(row);

            }
            tableModel.fireTableDataChanged();
            rs.close();
            ps.close();
            conn.close();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Lỗi:" + ex.getMessage());
        }
    }

