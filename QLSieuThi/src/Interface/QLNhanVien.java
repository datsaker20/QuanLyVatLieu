    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if (txtMaNV.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn đối tượng để xóa");

        } else {
            txtMaNV.setBackground(Color.white);
            if (sb.length() > 0) {
                JOptionPane.showInputDialog(this, sb);
                return;
            }
            if (JOptionPane.showConfirmDialog(this, "Xác Nhận Xóa Nhân Viên") == JOptionPane.NO_OPTION) {
                return;
            }
            try {
                ;
                NhanVienDao dao = new NhanVienDao();
                dao.Delete(txtMaNV.getText());
                JOptionPane.showMessageDialog(this, "Xóa thành công!");
                BangNhanVien();

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Lỗi:" + ex.getMessage());
            }

        }


    }//GEN-LAST:event_btnXoaActionPerformed

