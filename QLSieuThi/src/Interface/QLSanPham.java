    public void getCboNhaCungCap() {
        DefaultComboBoxModel cboModel = (DefaultComboBoxModel) cbNCC.getModel();
        for (NhaCungCap ncc : nccDao.getAll()) {
            cboModel.addElement(ncc);
        }
    }

    public void LamMoiFrom() {
        txtMaSP.setText("");
        txtTenSP.setText("");
        txtGiaBan.setText("");
        txtDVT.setText("");

    }

    public void fillData() {
        DefaultTableModel tbModel = (DefaultTableModel) tblSP.getModel();
        tbModel.setRowCount(0);
        for (SanPham sp : spDao.getAll()) {
            Object dataRow[] = new Object[5];
            dataRow[0] = sp.getMaHH();
            dataRow[1] = sp.getTenHH();
            dataRow[2] = sp.getGiaBan();
            dataRow[3] = sp.getDVT();
            dataRow[4] = sp.getMaNCC().getMaNCC();
            tbModel.addRow(dataRow);
        }

    }

    public SanPham getModel() throws ParseException {
        SanPham sp = new SanPham();
        sp.setMaHH(txtMaSP.getText());
        sp.setTenHH(txtTenSP.getText());
        sp.setGiaBan(Integer.parseInt(txtGiaBan.getText()));
        sp.setDVT(txtDVT.getText());
        NhaCungCap ncc = nccDao.getByName(cbNCC.getSelectedItem().toString());
        sp.setMaNCC(ncc);

        return sp;

    }

