    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if (txtMaSP.getText().equals("") || txtTenSP.getText().equals("") || txtGiaBan.getText().equals("") || txtDVT.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Vui Lòng Nhập Đầy Đủ Thông Tin");

        } else {

            try {
                SanPham sp = getModel();
                if (spDao.add(sp) > 0) {
                    JOptionPane.showMessageDialog(this, "Lưu Thành Công! ");
                    fillData();
                }

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Lỗi 6:" + ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnLuuActionPerformed

    private void tblSPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSPMouseClicked
        // TODO add your handling code here:
        int row = tblSP.getSelectedRow();
        if (row >= 0) {
            txtMaSP.setText(tblSP.getValueAt(row, 0).toString());
            txtTenSP.setText(tblSP.getValueAt(row, 1).toString());
            txtGiaBan.setText(tblSP.getValueAt(row, 2).toString());
            txtDVT.setText(tblSP.getValueAt(row, 3).toString());
            cbNCC.getModel().setSelectedItem(tblSP.getValueAt(row, 4));
        }
    }//GEN-LAST:event_tblSPMouseClicked

